    public void leerProductos(String FicheroP){
        try {
            FileReader fr = new FileReader(FicheroP);
            BufferedReader bfr = new BufferedReader(fr);
            String linea;
            String nombre;
            String categoria;
            double precio;
            int stock;
            
            try {
                while((linea = bfr.readLine()) != null){
                        String trozos[] = linea.split(";");
                        nombre = trozos[0];
                        categoria = trozos[1];
                        precio = Double.parseDouble(trozos[2]);
                        stock = Integer.parseInt(trozos[3]);
                        if(stock > 0){
                             Producto p = new Producto(nombre, categoria, precio, stock);
                             dlm_productos.addElement(p);
                        }
                    }      
            } catch (IOException ex) {
                Logger.getLogger(MiVentana.class.getName()).log(Level.SEVERE, null, ex);
            }
            
            
        } catch (FileNotFoundException ex) {
            Logger.getLogger(MiVentana.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    

