    private void btn_buscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_buscarActionPerformed
        String f = txt_filtrados.getText();
        boolean encontrado = false;
        
        if(f.isBlank()){
            JOptionPane.showMessageDialog(this, "Ingrese una categoría para filtrar");
        }
        dlm_filtrados.clear();
        
        for (int j = 0; j < dlm_productos.getSize(); j++) {
            Producto p = (Producto) dlm_productos.getElementAt(j);
            if(p.getCategoria().equalsIgnoreCase(f)){
                dlm_filtrados.addElement(p);
                encontrado = true;
            }
        }
        if(!encontrado){
            JOptionPane.showMessageDialog(this, "\n No hay ninguna coincidencia \n");  
        }
    }//GEN-LAST:event_btn_buscarActionPerformed
    
    public void calcularTotalPedido(Pedido p){
           // 1. Calcular el total (parte original)
    ArrayList listaProductos = p.getLineasPedido();
    double subtotal = 0.0, total = 0.0;
    
    for (int i = 0; i < listaProductos.size(); i++) {
        LineaPedido lp = (LineaPedido) listaProductos.get(i);
        Producto prod = (Producto) lp.getProducto();
        int c = lp.getCantidad();
        subtotal = prod.getPrecio() * c;
        total = total + subtotal;
    }
    
    p.setTotal(total);
    txt_total.setText(String.valueOf(total));
    
    // 2. Guardar en archivo (nueva funcionalidad integrada)
    try {
        FileWriter fw = new FileWriter("pedidos.txt", true); // true para append (añadir sin borrar)
        BufferedWriter bw = new BufferedWriter(fw);
        
        bw.write("--- NUEVO PEDIDO ---");
        bw.newLine();
        bw.write("Cliente: " + p.getCliente());
        bw.newLine();
        bw.write("Total: " + total + "€");
        bw.newLine();
        bw.write("Productos:");
        bw.newLine();
        
        for (LineaPedido linea : p.getLineasPedido()) {
            Producto prod = linea.getProducto();
            bw.write("- " + prod.getNombre() + 
                    " | Cantidad: " + linea.getCantidad() + 
                    " | Precio: " + prod.getPrecio() + "€");
            bw.newLine();
        }
        
        bw.write("----------------------------");
        bw.newLine();
        bw.close();
        
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(this, "Error al guardar el pedido: " + ex.getMessage());
        }
    }
