    private void btn_añadirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_añadirActionPerformed
          int i = lst_productos.getSelectedIndex();
        if (i == -1) {
            JOptionPane.showMessageDialog(this, "Error: Primero debe seleccionar un producto");
            return;
        }

        if (txt_cantidad.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Error: Debe introducir una cantidad");
            txt_cantidad.requestFocus();
            return;
        }

        Producto p = (Producto) dlm_productos.getElementAt(i);
        int cantidad = Integer.parseInt(txt_cantidad.getText().trim());
        
        if (cantidad <= 0) {
            JOptionPane.showMessageDialog(this, "Error: La cantidad debe ser mayor que cero");
            return;
        }

        if (cantidad > p.getStock()) {
            JOptionPane.showMessageDialog(this,  "Error: Stock insuficiente. Disponible: " + p.getStock());
            return;
        }

        int nStock = p.getStock() - cantidad;
        p.setStock(nStock);

        LineaPedido lp = new LineaPedido(p, cantidad);
        pedido.addlineaPedido(lp);

        if (nStock <= 0) {
            dlm_productos.removeElementAt(i); // Eliminar por índice
        }

        // 9. Actualizar interfaz
        lst_productos.repaint();
        txt_cantidad.setText(""); // Limpiar solo el campo de cantidad
        
    }//GEN-LAST:event_btn_añadirActionPerformed

